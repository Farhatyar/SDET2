package class6;

public class WhileLoop3 {
    public static void main(String[] args) {
        int n=1;
        while (n<=100){
            System.out.println(n);
            n++;
        }
        System.out.println("******************************");

        int m=1;
        while (m<=15){
            System.out.println(m);
            m+=2;
        }
        System.out.println("******************************");

        int o=5;
        while (o<=50){
            System.out.println(o);
            o+=5;
        }
        System.out.println("******************************");

    }
}
