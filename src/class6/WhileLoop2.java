package class6;

public class WhileLoop2 {
    public static void main(String[] args) {

        int n=1;

        while (n<6){
            System.out.println(n);
            n++;
        }

        int n2=0;
        while (n2<=10){
            System.out.println(n2);
            n2+=2;
        }
    }
}
